def extract_text_from_pdf(file_path):
    """
    Extracts raw text from a PDF file page by page.
    Returns complete text as a single string.
    """
    text = ""

    with open(file_path, "rb") as file:
        reader = PyPDF2.PdfReader(file)

        for page in reader.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + "\n"
    return text


#removing non english text here

def keep_english_text(text):
    """
    Removes non-ASCII characters.
    This helps remove Hindi text and encoding artifacts.
    """
    return text.encode("ascii", errors="ignore").decode()


#removing boilerplates from text here

def remove_boilerplate(text):
    """
    Removes common formatting artifacts such as:
    - Page numbers
    - Headers
    - Repeated volume information
    """

    # Remove page numbers like "Page 1 of 6"
    text = re.sub(r'Page\s*\d+\s*of\s*\d+', ' ', text)

    # Remove standalone numbers
    text = re.sub(r'\b\d+\b', ' ', text)

    # Remove repeated phrases
    text = re.sub(r'IIT Jodhpur', ' ', text, flags=re.IGNORECASE)

    return text



def clean_text(text):
    """
    Performs final cleaning:
    - Lowercasing
    - Removing punctuation
    - Removing extra spaces
    """

    # Convert to lowercase
    text = text.lower()

    # Remove punctuation
    text = text.translate(str.maketrans('', '', string.punctuation))

    # Remove multiple spaces
    text = re.sub(r'\s+', ' ', text)

    return text


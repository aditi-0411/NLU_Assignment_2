import re
import pdfplumber


complete_text = ""
#Preparing sentence-level corpus for Word2Vec training.
for pdf in pdf_files:
    with pdfplumber.open(pdf) as pdf_file:
        for page in pdf_file.pages:
            page_text = page.extract_text()
            if page_text:
                # keep punctuation here
                clean_ascii = page_text.encode("ascii", errors="ignore").decode()
                complete_text += clean_ascii + "\n"

# Now check punctuation exists
print("Does text contain period?", "." in complete_text)

# Split into sentences BEFORE cleaning punctuation
raw_sentences = re.split(r'[.!?]+\s+', complete_text)

print("Raw sentence count:", len(raw_sentences))

# Now clean each sentence
sentences = []

for sent in raw_sentences:
    sent = sent.lower()

    # Keep only alphabetic words
    tokens = re.findall(r'\b[a-zA-Z]{3,}\b', sent)

    if len(tokens) > 3:
        sentences.append(tokens)

print("Final usable sentences:", len(sentences))
print("Example:", sentences[0])

"""
Training CBOW Word2Vec model
"""

from gensim.models import Word2Vec

cbow_model = Word2Vec(
    sentences=sentences,
    vector_size=100,      # Embedding dimension
    window=5,             # Context window size
    min_count=2,          # Ignore rare words
    workers=4,
    sg=0,                 # 0 = CBOW
    negative=5,           # Negative samples
    epochs=10,
    compute_loss=True
)

print("CBOW model trained successfully.")
print("CBOW training loss:", cbow_model.get_latest_training_loss())
"""
Training Skip-gram with Negative Sampling
"""

skipgram_model = Word2Vec(
    sentences=sentences,
    vector_size=100,
    window=5,
    min_count=2,
    workers=4,
    sg=1,          # 1 = Skip-gram
    negative=5,
    epochs=10,
    compute_loss=True

)

print("Skip-gram model trained successfully.")
print("Skip-gram training loss:", skipgram_model.get_latest_training_loss())
"""
Hyperparameter experimentation for both models.
"""

embedding_dims = [50, 100, 200]
window_sizes = [3, 5]
negative_samples = [5, 10]

experiment_results = []

print("Starting hyperparameter experiments...")

for dim in embedding_dims:
    for window in window_sizes:
        for neg in negative_samples:

            model = Word2Vec(
                sentences=sentences,
                vector_size=dim,
                window=window,
                min_count=2,
                sg=1,   # experimenting on Skip-gram
                negative=neg,
                epochs=5,
                compute_loss=True
            )

            loss = model.get_latest_training_loss()

            experiment_results.append({
                "Dimension": dim,
                "Window": window,
                "Negative Samples": neg,
                "Training Loss": loss
            })

print("Experiments completed.")

for res in experiment_results:
    print(res)

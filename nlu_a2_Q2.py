# dataset
class NameDataset(Dataset):

    def __init__(self, names, char_to_idx, start_token, end_token):
        self.data = []

        for name in names:
            seq = start_token + name + end_token
            inp = seq[:-1]
            tgt = seq[1:]

            inp_idx = [char_to_idx[c] for c in inp]
            tgt_idx = [char_to_idx[c] for c in tgt]

            self.data.append((torch.tensor(inp_idx), torch.tensor(tgt_idx)))

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        return self.data[idx]


# collate function for padding
def collate_fn(batch):

    xs = [item[0] for item in batch]
    ys = [item[1] for item in batch]

    xs = nn.utils.rnn.pad_sequence(xs, batch_first=True, padding_value=pad_idx)
    ys = nn.utils.rnn.pad_sequence(ys, batch_first=True, padding_value=pad_idx)

    return xs, ys



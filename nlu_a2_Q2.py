def compute_novelty(generated_names, training_names):

    training_set = set(training_names)

    new_names = []

    for name in generated_names:

        if name not in training_set:
            new_names.append(name)

    novelty = len(new_names) / len(generated_names)

    return novelty


# Diversity

def compute_diversity(generated_names):

    unique_names = set(generated_names)

    diversity = len(unique_names) / len(generated_names)

    return diversity



import torch
import torch.nn as nn

class CharRNN(nn.Module):
    """
    Vanilla RNN model for character-level name generation.
    """

    def __init__(self, vocab_size, embed_size=32, hidden_size=64):
        super(CharRNN, self).__init__()

        self.embedding = nn.Embedding(vocab_size, embed_size, padding_idx=char_to_idx["<pad>"])
        self.rnn = nn.RNN(
            input_size=embed_size,
            hidden_size=hidden_size,
            batch_first=True
        )

        self.fc = nn.Linear(hidden_size, vocab_size)

    def forward(self, x):

        # Convert character indices → embeddings
        x = self.embedding(x)

        # Pass through RNN
        out, _ = self.rnn(x)

        # Predict next character
        out = self.fc(out)

        return out


# failure modes

def analyze_failures(generated_names):

    very_short = []
    repeated_chars = []
    strange_patterns = []

    for name in generated_names:

        # very short names
        if len(name) <= 2:
            very_short.append(name)

        # repeated characters
        if len(set(name)) < len(name) / 2:
            repeated_chars.append(name)

        # unusual character combinations
        if any(char not in "abcdefghijklmnopqrstuvwxyz" for char in name):
            strange_patterns.append(name)

    return very_short, repeated_chars, strange_patterns


